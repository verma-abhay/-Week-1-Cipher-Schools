public class IncreDecre {
    public static void main(String[] args) {
       //Post increment
        int m=5;
        int n=m++;
        System.out.println("m is "+m+" "+"n is "+n);
     //pre increment
         int p=5;
        int q=++p;
        System.out.println("p is "+p+" "+"q is "+q);

        //Decrement
        int a=5;
        int b=--a;               //this is pre decrement
        System.out.println("a "+a+" b "+b);
    }
    
}
